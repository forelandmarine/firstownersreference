function backMatterToMd(content) {
  const lines = [];
  lines.push(`---`);
  lines.push(`title: "Back matter"`);
  lines.push(`---`);
  lines.push("");
  lines.push(`# Glossary`);
  lines.push("");
  lines.push(`*Terms used across the chapters, set as a continuous reference.*`);
  lines.push("");
  const glossary = content.glossaryEntries
    .slice()
    .sort((a, b) => a.term.localeCompare(b.term));
  for (const entry of glossary) {
    lines.push(`**${escapeMd(entry.term)}**. ${escapeMd(entry.shortDefinition ?? "")}`);
    lines.push("");
  }
  lines.push(`# Colophon`);
  lines.push("");
  lines.push(`*Editorial principles, type, paper, and the publisher's own answers to the independence test.*`);
  lines.push("");
  return lines.join("\n");
}

