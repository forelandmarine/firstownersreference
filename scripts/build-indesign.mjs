/*
  Build an InDesign-importable export of the editorial content.

  Output:
    ~/Desktop/firstownersreference-indesign/
      00-cover.md
      00-cover.icml
      00-front-matter.md
      00-front-matter.icml
      01-reality-of-ownership.md
      01-reality-of-ownership.icml
      ... (one per chapter)
      back-matter.md
      back-matter.icml
      style-guide.md
      README.md

  Workflow for the designer:
    1. Open InDesign. Create a new document at 230 x 300 mm trim.
    2. Set up paragraph styles matching the names in style-guide.md.
    3. Use File > Place on each .icml to import the styled content.
    4. Flow the placed text frames across pages, applying paragraph styles
       as the styles import.

  ICML is generated by Pandoc from Markdown. Markdown is generated here
  from the lib/lead-essays.ts, lib/data-spreads.ts, lib/cases.ts,
  lib/guest-opinions.ts content via dynamic import.
*/

import fs from "node:fs";
import path from "node:path";
import os from "node:os";
import { execSync } from "node:child_process";
import { pathToFileURL } from "node:url";

const REPO_ROOT = "/Users/jack/firstownersreference";
const OUT_DIR = path.join(os.homedir(), "Desktop", "firstownersreference-indesign");
const SECTIONS_TS = path.join(REPO_ROOT, "lib/sections.ts");

// Ensure output directory exists
fs.mkdirSync(OUT_DIR, { recursive: true });

const log = (msg) => console.log(`[${new Date().toISOString().slice(11, 19)}] ${msg}`);

// Dynamically import the content files. Use tsx to handle .ts imports if needed.
async function loadContent() {
  // Compile TS files to a temp directory then import
  const tmpDir = path.join(REPO_ROOT, ".tmp-indesign-build");
  fs.mkdirSync(tmpDir, { recursive: true });

  // Use esbuild via the project's existing tooling
  execSync(
    `npx esbuild --bundle --platform=node --format=esm --outfile=${tmpDir}/content.mjs ${REPO_ROOT}/scripts/_content-loader.ts`,
    { stdio: "pipe" }
  );

  const mod = await import(pathToFileURL(`${tmpDir}/content.mjs`).href);
  return mod;
}

function escapeMd(s) {
  if (!s) return "";
  return String(s).replace(/([\\`*_{}\[\]()#+\-.!])/g, "\\$1").replace(/\u2019/g, "'").replace(/\u201C/g, '"').replace(/\u201D/g, '"');
}

function paragraphsToMd(paras) {
  const out = [];
  for (const p of paras ?? []) {
    if (typeof p === "string") {
      out.push(escapeMd(p));
      out.push("");
      continue;
    }
    if (p.type === "h2") {
      out.push(`## ${escapeMd(p.text)}`);
      out.push("");
      continue;
    }
    if (p.type === "blockquote") {
      const attribution = p.attribution ? `\n>\n> — ${escapeMd(p.attribution)}` : "";
      out.push(`> ${escapeMd(p.text)}${attribution}`);
      out.push("");
      continue;
    }
    if (p.type === "editorsNote") {
      out.push(`> **Editor's note.** ${escapeMd(p.text)}`);
      out.push("");
      continue;
    }
    if (p.type === "figure") {
      out.push(`![${escapeMd(p.alt ?? "")}](${p.src ?? ""})`);
      if (p.caption) {
        out.push(`*${escapeMd(p.caption)}*`);
      }
      out.push("");
      continue;
    }
  }
  return out.join("\n");
}

function chapterToMd(section, essay, caseStudy, dataSpread, guestOpinion) {
  const lines = [];
  lines.push(`---`);
  lines.push(`title: "Chapter ${String(section.number).padStart(2, "0")} · ${section.title}"`);
  lines.push(`---`);
  lines.push("");
  lines.push(`# Chapter ${String(section.number).padStart(2, "0")}`);
  lines.push("");
  lines.push(`# ${escapeMd(section.title)}`);
  lines.push("");
  lines.push(`*${escapeMd(section.standfirst)}*`);
  lines.push("");

  if (essay?.paragraphs) {
    lines.push(`## LEAD ESSAY`);
    lines.push("");
    lines.push(paragraphsToMd(essay.paragraphs));
    lines.push("");
  }

  if (dataSpread) {
    lines.push(`## DATA SPREAD`);
    lines.push("");
    lines.push(`### ${escapeMd(dataSpread.title)}`);
    lines.push("");
    lines.push(`*${escapeMd(dataSpread.standfirst)}*`);
    lines.push("");
    for (const block of dataSpread.blocks ?? []) {
      if (block.type === "h2") {
        lines.push(`#### ${escapeMd(block.text)}`);
        lines.push("");
      } else if (block.type === "paragraph") {
        lines.push(escapeMd(block.text));
        lines.push("");
      } else if (block.type === "table") {
        if (block.caption) {
          lines.push(`*${escapeMd(block.caption)}*`);
          lines.push("");
        }
        const head = (block.head ?? []).map((h) => escapeMd(h));
        lines.push(`| ${head.join(" | ")} |`);
        lines.push(`| ${head.map(() => "---").join(" | ")} |`);
        for (const row of block.rows ?? []) {
          lines.push(`| ${row.map((c) => escapeMd(c)).join(" | ")} |`);
        }
        lines.push("");
        if (block.sourceLine) {
          lines.push(`*Source: ${escapeMd(block.sourceLine)}*`);
          lines.push("");
        }
      } else if (block.type === "kv") {
        if (block.caption) {
          lines.push(`*${escapeMd(block.caption)}*`);
          lines.push("");
        }
        for (const row of block.rows ?? []) {
          lines.push(`**${escapeMd(row.label)}**: ${escapeMd(row.value)}`);
          lines.push("");
        }
      } else if (block.type === "note") {
        lines.push(`> **Note.** ${escapeMd(block.text)}`);
        lines.push("");
      } else if (block.type === "chart") {
        lines.push(`*[Chart: ${escapeMd(block.chartId)}]*`);
        lines.push("");
      }
    }
  }

  if (guestOpinion) {
    lines.push(`## GUEST OPINION`);
    lines.push("");
    lines.push(`### In conversation with ${escapeMd(guestOpinion.contributor)}`);
    lines.push("");
    lines.push(`*${escapeMd(guestOpinion.contributorRole)}. ${escapeMd(guestOpinion.intro)}*`);
    lines.push("");
    for (const qa of guestOpinion.questions ?? []) {
      lines.push(`**${escapeMd(qa.question)}**`);
      lines.push("");
      for (const a of qa.answer ?? []) {
        lines.push(escapeMd(a));
        lines.push("");
      }
    }
  }

  if (caseStudy) {
    lines.push(`## CASE STUDY`);
    lines.push("");
    lines.push(`### ${escapeMd(caseStudy.title)}`);
    lines.push("");
    lines.push(`*${escapeMd(caseStudy.standfirst)}*`);
    lines.push("");
    for (const m of caseStudy.meta ?? []) {
      lines.push(`**${escapeMd(m.label)}**: ${escapeMd(m.value)}`);
      lines.push("");
    }
    lines.push(paragraphsToMd(caseStudy.paragraphs));
    lines.push("");
    if (caseStudy.disclosure) {
      lines.push(`*Disclosure. ${escapeMd(caseStudy.disclosure)}*`);
      lines.push("");
    }
  }

  return lines.join("\n");
}

