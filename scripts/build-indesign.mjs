function mdToIcml(mdPath, icmlPath) {
  try {
    execSync(`pandoc "${mdPath}" -o "${icmlPath}" -t icml`, { stdio: "pipe" });
    return true;
  } catch (err) {
    log(`  ! Pandoc failed for ${path.basename(mdPath)}: ${err.message}`);
    return false;
  }
}

async function main() {
  log("Loading content...");

  // Create a small content loader that exports everything we need
  const loaderPath = path.join(REPO_ROOT, "scripts/_content-loader.ts");
  fs.writeFileSync(
    loaderPath,
    `// Auto-generated content loader for build-indesign.mjs
export { sections } from "../lib/sections";
export { leadEssays } from "../lib/lead-essays";
export { dataSpreads } from "../lib/data-spreads";
export { cases } from "../lib/cases";
export { guestOpinions } from "../lib/guest-opinions";
export { glossaryEntries } from "../lib/glossary";
`
  );

  const content = await loadContent();
  const { sections, leadEssays, dataSpreads, cases, guestOpinions } = content;

  // Front matter
  log("Writing front matter...");
  const fmPath = path.join(OUT_DIR, "00-front-matter.md");
  fs.writeFileSync(fmPath, frontMatterToMd(content));
  mdToIcml(fmPath, fmPath.replace(/\.md$/, ".icml"));

  // Chapters
  for (const section of sections) {
    const num = String(section.number).padStart(2, "0");
    const slug = section.slug;
    log(`Writing chapter ${num} - ${section.title}`);
    const md = chapterToMd(
      section,
      leadEssays?.[slug],
      cases?.[slug],
      dataSpreads?.[slug],
      guestOpinions?.[slug]
    );
    const mdPath = path.join(OUT_DIR, `${slug}.md`);
    fs.writeFileSync(mdPath, md);
    mdToIcml(mdPath, mdPath.replace(/\.md$/, ".icml"));
  }

  // Back matter
  log("Writing back matter...");
  const bmPath = path.join(OUT_DIR, "back-matter.md");
  fs.writeFileSync(bmPath, backMatterToMd(content));
  mdToIcml(bmPath, bmPath.replace(/\.md$/, ".icml"));

  // Style guide
  fs.writeFileSync(path.join(OUT_DIR, "style-guide.md"), styleGuide());

  // README
  fs.writeFileSync(path.join(OUT_DIR, "README.md"), readme());

  // Cleanup
  fs.rmSync(path.join(REPO_ROOT, ".tmp-indesign-build"), { recursive: true, force: true });
  fs.rmSync(loaderPath, { force: true });

  log(`Done. Output at ${OUT_DIR}`);
  log(`  ${fs.readdirSync(OUT_DIR).length} files`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
